$(document).ready(function(){	
	$("#date-box").datepicker({altField: '.db-date', altFormat: 'yy-mm-dd'});
	
	$("#date-box-sharing").datepicker({
		altField: '.db-date', 
		altFormat: 'yy-mm-dd',
		onSelect: function (dateText, inst) {
			d = new Date( dateText ).getDay();
			$.ajax({
		    	url: "/teachers_cbx",
		        data: {
		          weekday : d
		        },
		        type: "GET",
				success: function(result)
				{
					$('#teacher_check_box').html(result).show();
				}
		   	});
			
		}
	});
});